let n = 16;
let mid = Math.floor(n / 2);

for (let i = 0; i < n; i++) {
  let row = "";
  for (let j = 0; j < n; j++) {
    if (
      (i === 0 && (j === 0 || j >= mid)) ||
      (i === 1 && (j === 0 || j >= mid)) ||
      j === mid ||
      (j == 0 && i <= mid) ||
      (j == n - 1 && i >= mid) ||
      i === mid ||
      i === mid - 1 ||
      (i === n - 1 && (j === n - 1 || j <= mid)) ||
      (i === n - 2 && (j === n - 1 || j <= mid))
    ) {
      row += "** ";
    } else {
      row += "   ";
    }
  }
  console.log(row);
}
